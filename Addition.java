public class Addition
{
    public static void main(String[]args)
    {
        int a=20;
        int b=60;
        int s=a+b;
        int m=a*b;
        int l=a-b;
        int p=a/b;
        System.out.println(a + "+" + b + "=" + s);
        System.out.println(a + "*" + b + "=" + m);
        System.out.println(a + "-" + b + "=" + l);
        System.out.println(a + "/" + b + "=" + p);
    }
}